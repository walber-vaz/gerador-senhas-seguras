// Array é um tipo de dado que armazena uma lista de valores
const valores = [7.7, 8.9, 6.3, 9.2];

// Acessando um valor do array
console.log(valores[0], valores[3]);

// Acessando um valor que não existe
console.log(valores[4]);

// Adicionando um valor no array
valores[4] = 10;

// Adicionando um valor no array de forma dinâmica
valores[10] = 11;

// Imprimindo o array
console.log(valores);
